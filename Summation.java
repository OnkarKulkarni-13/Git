public class Summation {
    public static void main(String[] args) {
        int a = 10, b = 20, sum = 0;
        sum = a + b;
        System.out.println("Adding 2 Numbers :-> " + sum);

        // Adding 3 numbers
        int c = 30;
        sum = a + b + c;
        System.out.println("After Adding 3 numbers :-> " + sum);

    }
}
